package com.dinens.citron.p.admin.menu.domain;

import com.dinens.citron.p.admin.common.domain.ExtendVO;

public class MenuLocaleVO extends ExtendVO {

	private static final long serialVersionUID = -8650541548177513036L;
	
	private String menuId;
	private String localeCode;
	private String menuName;
	private String displayOrder;
	private String createId;
	private String createTime;
	
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getLocaleCode() {
		return localeCode;
	}
	public void setLocaleCode(String localeCode) {
		this.localeCode = localeCode;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getDisplayOrder() {
		return displayOrder;
	}
	public void setDisplayOrder(String displayOrder) {
		this.displayOrder = displayOrder;
	}
	public String getCreateId() {
		return createId;
	}
	public void setCreateId(String createId) {
		this.createId = createId;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

}
